@admin_router.message(Command("admin"), F.text| F.command)
@admin_router.callback_query(lambda c: c.data.startswith("admin"))
async def admin_handler(message_or_callback: types.Union[types.Message, CallbackQuery]):
    if isinstance(message_or_callback, types.Message):
        # –ï—Å–ª–∏ —ç—Ç–æ —Å–æ–æ–±—â–µ–Ω–∏–µ
        message = message_or_callback
        await message.answer(text="–î–æ–±–∞–≤—Ç–µ —Ç–æ–≤–∞—Äüëá", reply_markup=inline_product_add_dell_kb)
    elif isinstance(message_or_callback, CallbackQuery):
        # –ï—Å–ª–∏ —ç—Ç–æ –∫–æ–ª–±—ç–∫-–∑–∞–ø—Ä–æ—Å
        callback_query = message_or_callback
        await callback_query.message.answer(text="–î–æ–±–∞–≤—Ç–µ —Ç–æ–≤–∞—Äüëá", reply_markup=inline_product_add_dell_kb)


@admin_router.callback_query(F.data == 'all_products_list')
async def product_list_all(callback: types.CallbackQuery, session: AsyncSession):
    for product in await orm_get_products(session):
        await callback.message.answer_photo(
            product.image,
            caption=f"<strong>{product.name}</strong>\n"
                    f"{product.description}\n"
                    f"–°—Ç–æ–∏–º–æ—Å—Ç—å: {round(product.price, 2)}",
            reply_markup=get_callback_btns(btns={
                '–£–¥–∞–ª–∏—Ç—å': f"delete_{product.id}",
                '–ò–∑–º–µ–Ω–∏—Ç—å': f"change_{product.id}",
            })
        )
    await callback.message.answer("–û–∫, –≤–æ—Ç —Å–ø–∏—Å–æ–∫ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–π.")


@admin_router.callback_query(StateFilter(None), F.data.startswith('change_'))
async def change_product(callback: types.CallbackQuery, state: FSMContext, session: AsyncSession):

    product_id = callback.data.split("_")[-1]
    product_for_change = await orm_get_product(session, int(product_id))

    AddProduct.product_for_change = product_for_change

    await callback.answer()
    await callback.message.answer("–í–≤–µ–¥–∏—Ç–µ –Ω–∞–∑–≤–∞–Ω–∏–µ —Ç–æ–≤–∞—Ä–∞")
    await state.set_state(AddProduct.name)


@admin_router.callback_query(F.data.startswith('delete_'))
async def delete_product(callback: types.CallbackQuery, session: AsyncSession):

    product_id = callback.data.split("_")[-1]
    await orm_delete_product(session, int(product_id))

    await callback.answer('–ü—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–µ —É–¥–∞–ª–µ–Ω–æ.')
    await callback.message.answer('–ü—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–µ —É–¥–∞–ª–µ–Ω–æ.')


