@admin_router.message(AddProduct.image, or_f(F.photo, F.text == '.'))
async def add_image(message: types.Message, state: FSMContext, session: AsyncSession):
    if message.text == ".":
        await state.update_data(image=AddProduct.product_for_change.image)
    else:
        await state.update_data(image=message.photo[-1].file_id)

    data = await state.get_data()
    try:
        if AddProduct.product_for_change:
            await orm_update_product(session, AddProduct.product_for_change.id, data)
            await message.answer("–ü—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–µ –∏–∑–º–µ–Ω–µ–Ω–æ.", reply_markup=inline_product_add_dell_kb)
        else:
            await orm_add_product(session, data)
            await message.answer("–ü—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–µ –¥–æ–±–∞–≤–ª–µ–Ω–æ.", reply_markup=inline_product_add_dell_kb)
        await state.clear()

    except Exception as e:
        await message.answer(
            f"–û—à–∏–±–∫–∞: \n{str(e)}\n–û–±—Ä–∞—Ç–∏—Å—å –∫ –ø—Ä–æ–≥–µ—Ä—É. –ò–º–µ–π –≤ –≤–∏–¥—É, —á—Ç–æ —ç—Ç–æ –Ω–µ –±–µ—Å–ø–ª–∞—Ç–Ω–æ.", reply_markup=inline_product_add_dell_kb
        )
    AddProduct.product_for_change = None


